public class Ishchi {
   private String ism,familya,yoshi,jinsi,boyi,malumoti,tajribasi;

    public Ishchi() {
    }

    public Ishchi(String ism, String familya, String yoshi, String jinsi, String boyi, String malumoti, String tajribasi) {
        this.ism = ism;
        this.familya = familya;
        this.yoshi = yoshi;
        this.jinsi = jinsi;
        this.boyi = boyi;
        this.malumoti = malumoti;
        this.tajribasi = tajribasi;
    }

    public String getIsm() {
        return ism;
    }

    public void setIsm(String ism) {
        this.ism = ism;
    }

    public String getFamilya() {
        return familya;
    }

    public void setFamilya(String familya) {
        this.familya = familya;
    }

    public String getYoshi() {
        return yoshi;
    }

    public void setYoshi(String yoshi) {
        this.yoshi = yoshi;
    }

    public String getJinsi() {
        return jinsi;
    }

    public void setJinsi(String jinsi) {
        this.jinsi = jinsi;
    }

    public String getBoyi() {
        return boyi;
    }

    public void setBoyi(String boyi) {
        this.boyi = boyi;
    }

    public String getMalumoti() {
        return malumoti;
    }

    public void setMalumoti(String malumoti) {
        this.malumoti = malumoti;
    }

    public String getTajribasi() {
        return tajribasi;
    }

    public void setTajribasi(String tajribasi) {
        this.tajribasi = tajribasi;
    }
}
